num = int(input('Escreva um número qualquer: '))

print(f'O dobro desse número é {num * 2} e seu triplo é {num * 3}.')
print(f'Sua potência elevada a 2 é {num ** 2} e sua raiz quadrada é {num ** (1/2)}.')
print(f'Sua potência elevada a 3 é {num ** 3} e sua raiz cúbica é {num ** (1/3)}')
print(f'A versão simplificada de sua raiz quadrada é {num ** (1/2):.3f} \ne de sua raiz cúbica é {num ** (1/3):.3f}')