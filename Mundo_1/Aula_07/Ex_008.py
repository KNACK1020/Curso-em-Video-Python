# Convertendo metros para centímetros e milímetros. 
medida = float(input('Digite qualquer medida, que seja em metros: '))
medida_centimetros = medida * 100
print(f'Essa mesma medida em centímetros fica {medida_centimetros}')
medida_milimetros = medida * 100 * 100
print(f'Já em milimetros, fica {medida_milimetros}')